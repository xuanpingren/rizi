int
get_form_input(struct form* info)
{
    CGI_varlist* varlist;
    CGI_value* value;
    const char* name;
    int i;

    if ((varlist = CGI_get_all(0)) == 0) {
        printf("无数据\r<br>");
        return 0;
    }

    info->s = 1;
    info->e = 1;
    info->detail = 0;
    info->display_gl = 0;

    for (name = CGI_first_name(varlist); name != 0;
         name = CGI_next_name(varlist)) {

        value = CGI_lookup_all(varlist, 0);

        for (i = 0; value[i] != 0; i++) {
            if (strcmp(name, "start") == 0)
                info->s = atoi(value[i]);
            if (strcmp(name, "end") == 0)
                info->e = atoi(value[i]);
            if (strcmp(name, "detail") == 0)
                info->detail = atoi(value[i]);
            if (strcmp(name, "gl") == 0)
                info->display_gl = atoi(value[i]);
            if (strcmp(name, "last_name") == 0)
	      strcpy(info->x, value[i]);
            if (strcmp(name, "bazi") == 0)
	      strcpy(info->bazi, value[i]);
        }
    }

    CGI_free_varlist(varlist); /* free variable list */
    return 0;
}

/*
 * 给定起迄时间，输出干支纪年，秦纪年，公元纪年。flag不等于0则显示干支表。 
 * 秦始皇统一中国为庚辰年。 
 */
void
get_gan_zhi(int start, int end, int flag)
{
    int i,
        j,
        p,
        q;
    int t = 6,
        d = 4; /* 秦1年的干支编码 */
    if (start < 1)
        start = 1;
    for (i = 1; i <= end; i++) {
        j = i - 222;
        if (j >= 0)
            j = i - 221;
        t = t % 10;
        d = d % 12;
        if (i >= start) {
            printf("%s%s    秦%d年        <font size=-1 color=grey>(%d)</font><br/>\n",
                   tian_gan[t], di_zhi[d], i, j);
            if (flag == 1) {
                for (p = 0; p < 10; p++) {
                    if (p != t)
                        printf("%s ", tian_gan[p]);
                    else
                        printf("<span class=\"highlite\">%s</span> ",
                               tian_gan[p]);
                }
                printf("<br/>\n");
                for (q = 0; q < 12; q++) {
                    if (q != d)
                        printf("%s ", di_zhi[q]);
                    else
                        printf("<span class=\"highlite\">%s</span> ",
                               di_zhi[q]);
                }
                printf("<br/><br/>\n");
            }
        }
        t++;
        d++;
    }
}

/*
 * 年干支。 y >= 1。 秦始皇统一六国，y=1。 
 */
struct gz
get_gan_zhi_nian(int y)
{
    /*
     * 秦1年的干支编号未6和4 
     */
    int tg = (6 + (y - 1)) % 10;
    int dz = (4 + (y - 1)) % 12;
    struct gz x;
    x.g = tg;
    x.z = dz;

    return x;
}

/*
 * 日干支。 
 */
struct gz
get_gan_zhi_ri(struct tm* t)
{
    /*
     * 公元1976年9月9日，毛泽东逝世日甲子 
     */
    double s;
    int diff;
    struct tm kg = { 0 };
    struct gz x;

    kg.tm_year = 76;
    kg.tm_mon = 9 - 1;
    kg.tm_mday = 9;

    s = difftime(mktime(t), mktime(&kg));

    diff = (int)(s / (24 * 60 * 60));

    x.g = (0 + diff) % 10;
    x.z = (0 + diff) % 12;

    return x;
}

