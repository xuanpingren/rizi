struct baby_name
select_baby_name(int mode, char *last_name, char *bazi, struct gz year, struct gz date)
{
  /*  
   * 先从简单的开始。 
   * 随机选几个字。先决定一字名或二字名， 再决定选那本书，后决
   * 定选哪个字。选字注意阴阳搭配，音调搭配，五行相生。 
   * 以后的话 考虑增加模式1234
   * mode 0： 随机模式
   * mode 1： 经济模式 考虑阴阳 但不考虑五行
   * mode 2： 加强模式 考虑五行 但不考虑八字
   * mode 3： 无忧模式 考虑八字 但不考虑音调
   * mode 4： 入神模式 考虑音调
   */
  int num_zi = 2; /* 取几个字的名字 */
  int i = 0, 
      j;
  struct zi onez;
  struct baby_name bn;

  strcpy(bn.xing.z, last_name);
  srand(time(NULL));
  while (i < NUM_CANDIDATE_NAME) {
    num_zi = rand() % 2 + 1; 
    for (j = 0; j < num_zi; j++) {
      onez = zuo_zhuan_zi[rand() % (sizeof(zuo_zhuan_zi) / sizeof(zuo_zhuan_zi[0]))];
      bn.ming[i][j] = onez;
    }
    bn.zi_count[i] = num_zi;
    i++;
  }

  return bn;
}


